<script type="text/javascript">
    $(function () {
        $("#casaroyal_registration_form").validate({

            rules: {

                name: {
                    required: true
                },
                mobile: {
                    required: true,
                    minlength: 10,
                    maxlength: 20
                },
                email: {
                    required: true,
                    email: true
                },
                password: {
                    required: true,
                    minlength: 6,
                    maxlength: 15
                }
            },
            submitHandler: function (form) {
                var registered_name = $('#name').val();
                var registered_mobile = $('#mobile').val();
                var registered_email = $('#email').val();
                var registered_password = $('#password').val();

                $.ajax({
                    url: 'home/user_register',
                    type: "post",
                    data: {name: registered_name, mobile: registered_mobile, email: registered_email, password: registered_password},
                    success: function (response) {

                        if (response === 'sucess') {
//                            jQuery("#register").slideUp("fast", function () {
//                                jQuery("#otp").slideDown("fast");
//                            });
                            location.reload(true);
                            exit();
                        } else if (response === 'exists') {
                            jQuery("#overlay").removeClass('open');
                            swal({
                                title: "Account Exists",
                                text: "Email or Mobile no already exists pleaes login to view your account",
                                timer: 2500,
                                type: "warning",
                                showConfirmButton: false
                            });
                        } else {
                            jQuery("#overlay").removeClass('open');
                            swal({
                                title: "Something Went Wroong",
                                text: "Plese try again after some time",
                                timer: 2500,
                                type: "warning",
                                showConfirmButton: false
                            });
                        }
//					 console.log(response);
                    },
                    error: function ()
                    {
                        alert("failure");
                    }
                });
            },
            messages: {
                registered_name: {
                    required: "Enter your Name"
                },
                registered_mobile: {
                    required: "Enter your Mobile"
                },
                registered_email: {
                    required: "Enter your Email"
                },
                registered_password: {
                    required: "Enter Registered Password ",
                    minlength: "Minimum 6 Characters required",
                    maxlength: "Maximum 32 Characters only"
                }
            }
        });
    });

</script>

